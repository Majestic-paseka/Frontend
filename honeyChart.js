// Получает контекст элемента canvas с id="honeyChart"
const ctx = document.getElementById("honeyChart").getContext("2d");

// Инициализация графика с данными
const honeyChart = new Chart(ctx, {
  type: "line", // можно заменить на "bar" для столбчатой диаграммы, ну или нет, как Игорь захочет
  data: {
    labels: ["Весна", "Лето", "Осень", "Зима"], // Метки, обозначающие сезоны
    datasets: [{
      label: "Урожайность мёда (кг)", // Подпись к наборам данных
      data: [50, 65, 45, 30], // Примерные данные урожайности по сезонам
      backgroundColor: "rgba(255, 193, 7, 0.5)", // Полупрозрачный фон (желтый оттенок)
      borderColor: "#FFC107", // Цвет линии — тот же желтый цвет
      borderWidth: 2,
      fill: true // Заполнение области под линией
    }]
  },
  options: {
    responsive: true,
    scales: {
      y: {
        beginAtZero: true, // Начинаем ось Y с нуля
        ticks: {
          stepSize: 10 // Интервал между делениями оси Y
        }
      }
    },
    plugins: {
      legend: {
        position: "top"
      },
      title: {
        display: true,
        text: "Урожайность мёда по сезонам"
      }
    }
  }
});

